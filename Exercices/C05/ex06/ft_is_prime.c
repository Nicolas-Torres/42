int	ft_is_prime(int nb)
{
	int i;
	int a;

	i = 2;
	a = 1;
	if (nb <= 1)
		return (0);
	while (i < nb / a)
	{
		i++;
		a++;
		if (nb % a == 0)
			return (0);
	}
	return (1);
}
